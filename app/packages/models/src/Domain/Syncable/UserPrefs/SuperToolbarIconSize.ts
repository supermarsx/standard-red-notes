/**
 * Standard Red Notes: user-selectable size for the Super (Lexical) editor
 * toolbar icons. `Small` is the (slightly reduced) default; `Medium` matches the
 * previous default size, and `Large` makes the toolbar icons noticeably bigger
 * for easier tap targets / accessibility.
 */
export enum SuperToolbarIconSize {
  Small = 'Small',
  Medium = 'Medium',
  Large = 'Large',
}
